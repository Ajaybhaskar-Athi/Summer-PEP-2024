function filterQuestionsWithAnswers(questionData, answerData) {
  const filteredData = {};

  for (const questionId in questionData) {
    if (answerData.answers.hasOwnProperty(questionId)) {
      filteredData[questionId] = {
        question: questionData[questionId],
        answer: answerData.answers[questionId]
      };
    }
  }

  return filteredData;
}

